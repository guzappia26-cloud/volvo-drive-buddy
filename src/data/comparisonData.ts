export function getVolvoModel(modelName: string): VolvoModelData | undefined {
  return volvoModels.find(m => m.model === modelName);
}

export function getCompetitorBrands(modelName: string): string[] {
  const model = getVolvoModel(modelName);
  if (!model) return [];
  return [...new Set(model.competitors.map(c => `${c.brand} ${c.model}`))];
}
